@OptIn(ExperimentalPagerApi::class)
@Composable
fun MainScreens() {
    val tabs = listOf(
        TabItem.Home,
        TabItem.Consoles,
        TabItem.Packages,
        TabItem.Ftp,
        TabItem.Settings
    )
    val pagerState = rememberPagerState()
    Scaffold(
        topBar = { TopBar() },
    ) { padding ->
        Column(modifier = Modifier.padding(padding)) {
            Tabs(tabs = tabs, pagerState = pagerState)
            TabsContent(tabs = tabs, pagerState = pagerState)
        }
    }
}